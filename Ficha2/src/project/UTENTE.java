package project;

public class UTENTE {

	
	public class Utente {	
		private String nome;	 
		private int idade;	
		private float altura;	
		private int peso;	
		
		
		public Utente(String nome, int idade, float altura, int peso){
			this.nome = nome;
			this.idade = idade;
			this.altura = altura;
			this.peso = peso;
			
		}
	}
}
